"""
Exercício 2 - Funções: Com Retorno
Objetivo: Receber parâmetros e retornar o valor para uso posterior.
"""


def somar(n1: float, n2: float, n3: float) -> float:
    """Retorna a soma de três números."""
    return n1 + n2 + n3


# Chamada
breakpoint()
resultado = somar(5, 7, 6)
print(f"O resultado retornado foi: {resultado}")
